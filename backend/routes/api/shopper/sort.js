function dynamicSort(property, order) {
  let sort_order = 1;
  if (order === 'descending') {
    sort_order = -1;
  }
  return function (e1, e2) {
    const property1 = getProperty(property, e1);
    const property2 = getProperty(property, e2);
    if (property1 < property2) {
      return -1 * sort_order;
    } else if (property1 > property2) {
      return 1 * sort_order;
    } else {
      return 0 * sort_order;
    }
  };
}

